LOCAL_REGISTRY=""
NAMESPACE="eclipse-che"
DASHBOARD_IMAGE="quay.io/eclipse/che-dashboard:pr-1442"
CHE_OPERATOR_IMAGE="quay.io/eclipse/che-operator:next"
MODE="full"
QUAY_CREDS="${QUAY_CREDS:-}"
