while [[ $# -gt 0 ]]; do
    case $1 in
        --kubeconfig)
            KUBECONFIG_FILE="$2"
            shift 2
            ;;
        --registry)
            LOCAL_REGISTRY="$2"
            shift 2
            ;;
        --namespace)
            NAMESPACE="$2"
            shift 2
            ;;
        --dashboard-image)
            DASHBOARD_IMAGE="$2"
            shift 2
            ;;
        --mirror-from-namespace)
            MIRROR_FROM_NAMESPACES+=("$2")
            shift 2
            ;;
        --mode)
            MODE="$2"
            shift 2
            ;;
        --quay-creds)
            QUAY_CREDS="$2"
            shift 2
            ;;
        --skopeo-log-level)
